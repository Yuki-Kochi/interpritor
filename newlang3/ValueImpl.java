package newlang3;

public class ValueImpl {

	int iValue=0;
	double dValue=0.0;
	String SValue="";
	boolean bValue=false;
	ValueType vType;


//  実装すべきコンストラクタ
	public ValueImpl(String s){
		vType=ValueType.STRING;
		SValue=s;
		try {
			iValue=Integer.parseInt(s);
		} catch (Exception e){
			iValue=0;
		}
		try {
			dValue=Double.parseDouble(s);
		} catch (Exception e){
			iValue=0;
		}
		if (s!=""){
			bValue=true;
		}
	}

	public ValueImpl(int i){
		vType=ValueType.INTEGER;
		SValue=String.valueOf(i);
		iValue=i;
		dValue=(double)i;
		if (i!=0){
			bValue=true;
		}
	}

	public ValueImpl(double d){
		vType=ValueType.DOUBLE;
		SValue=String.valueOf(d);
		dValue=d;
		iValue=(int)d;
		if (d!=0.00){
			bValue=true;
		}
	}

	public ValueImpl(boolean b){
		vType=ValueType.BOOL;
		SValue=String.valueOf(b);
		if (b==true){
			bValue=true;
			iValue=1;
			dValue=1.00;
		}
	}

//	public String get_sValue();

	// ストリング型で値を取り出す。必要があれば、型変換を行う。
	public String getSValue(){
		return SValue;
	}

	// 整数型で値を取り出す。必要があれば、型変換を行う。
    public int getIValue(){
		return iValue;
	}

	// 小数点型で値を取り出す。必要があれば、型変換を行う。
	public double getDValue(){
		return dValue;
	}

     public boolean getBValue(){
		return bValue;
	}


    public ValueType getType(){
		return vType;
	}
}
 